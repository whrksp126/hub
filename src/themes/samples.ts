// 새 글 생성 시 채워줄 예시 Plate 본문(안전한 기본 노드만 사용).
// 테마(에디토리얼/매거진/칼럼/피처/사이드룰)의 구조 — 도입부·소제목·인용구 — 가
// 한눈에 드러나도록 현실적인 분량/형태로 구성.
const p = (...children: { text: string; bold?: boolean }[]) => ({ type: 'p', children })
const t = (text: string) => ({ text })
const b = (text: string) => ({ text, bold: true })
const h = (level: 2 | 3, text: string) => ({ type: `h${level}`, children: [{ text }] })
const quote = (text: string) => ({ type: 'blockquote', children: [{ text }] })

// 테마 미리보기/드래프트의 데크(stand-first)로 쓰이는 한 줄 요약
export const blogSampleExcerpt =
  '작은 결정 하나가 어떻게 전체 구조를 바꾸는지, 직접 겪은 과정을 따라가며 정리했습니다.'

